    function GotonextTab(value, object){
        btn_number = value;
        change_collapse = jQuery('.whcom_collapse_button_' + btn_number);
        if (btn_number === '2'){
            jQuery('#wcop_additional_services').css("display","block");
        }
        jQuery('.wcop_sp_section.activated').removeClass('activated');
        change_collapse.parent().parent().next().addClass('activated');
        change_collapse.parent().parent().addClass('wcop_collapse_success');
    }

</script>